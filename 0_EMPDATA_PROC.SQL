CREATE OR REPLACE PROCEDURE EMPDATA_VPAWAR(EMPNO IN NUMBER)
IS
FNAME VARCHAR2(30);
LNAME VARCHAR2(30);
UNAME VARCHAR2(30);
EMEL VARCHAR2(30);
CURSOR DATA_CURSOR(EMPNUM NUMBER) IS SELECT FIRST_NAME, LAST_NAME, UNIQUE_NAME, EMAIL FROM SRM_RESOURCES WHERE ID=EMPNUM;
BEGIN
OPEN DATA_CURSOR(EMPNO);
FETCH DATA_CURSOR INTO FNAME,LNAME,UNAME,EMEL;
DBMS_OUTPUT.PUT_LINE(FNAME);
DBMS_OUTPUT.PUT_LINE(LNAME);
DBMS_OUTPUT.PUT_LINE(UNAME);
DBMS_OUTPUT.PUT_LINE(EMEL);
CLOSE DATA_CURSOR;
END;

BEGIN
EMPDATA_VPAWAR(5023034);
END;