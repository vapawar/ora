CREATE FUNCTION SAL_UP (EMPNO NUMBER)
RETURN NUMBER
IS
NEW_SAL NUMBER(10,2);
OLD_SAL NUMBER(10,2);
BEGIN
SELECT SALARY INTO OLD_SAL FROM EMP26 WHERE ID=EMPNO;
NEW_SAL:= OLD_SAL + OLD_SAL*0.10;
UPDATE EMP26 SET SALARY=NEW_SAL WHERE ID=EMPNO;
DBMS_OUTPUT.PUT_LINE('SALARY OF EMPLOYEE '||EMPNO||' IS UPDATED TO '||NEW_SAL);
END;
